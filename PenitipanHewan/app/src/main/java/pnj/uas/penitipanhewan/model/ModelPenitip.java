package pnj.uas.penitipanhewan.model;

public class ModelPenitip {
    String id;
    String nama;
    String telepon;
    String hewan;

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getNama() {
        return nama;
    }

    public void setNama(String nama) {
        this.nama = nama;
    }

    public String getTelepon() { return telepon;   }

    public void setTelepon(String telepon) { this.telepon = telepon;   }

    public String getHewan() { return hewan;   }

    public void setHewan(String hewan) { this.hewan = hewan;   }

}
